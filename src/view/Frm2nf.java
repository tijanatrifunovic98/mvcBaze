    private void btnUnesiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnesiActionPerformed
        try {
            Proizvod proizvod = new Proizvod();
            proizvod.setSifraProizvoda(Long.parseLong(txtSifraProizvoda.getText().trim()));

            proizvod.setNazivProizvoda(txtNazivProizvoda.getText().trim());
            proizvod.setZapremina(Double.parseDouble(txtZapremina.getText().trim()));
            proizvod.setOpis(txtOpis.getText().trim());
            proizvod.setTipProizvoda(Long.parseLong(txtTipProizvoda.getText().trim()));
            proizvod.setJedMereStr(txtJedinicaMereStr.getText().trim());

            Controller.getInstance().addProizvod(proizvod);
            JOptionPane.showMessageDialog(this, "Proizvod successfully saved!", "Insert proizvod", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            ViewController.getInstance().openFrm2nf();

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Proizvod not saved!", "Insert proizvod", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnUnesiActionPerformed

