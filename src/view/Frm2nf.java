    private void prepareView() throws Exception {
        setTitle("2NF");
        filTableProizvod();
        fillTableCena();
    }

    private void filTableProizvod() throws Exception {
        List<Proizvod> proizvod = Controller.getInstance().getAllProizvod();
        TableProizvod model = new TableProizvod(proizvod);
        tblProizvod.setModel(model);
    }

    private void fillTableCena() throws Exception {
        List<Cena> cena = Controller.getInstance().getAllCena();
        TableCena model = new TableCena(cena);
        tblCena.setModel(model);
    }

    private void popnuCenu() {

        int c = tblCena.getSelectedRow();
        TableCena tc = (TableCena) tblCena.getModel();
        Cena cena = tc.getCena(c);

        txtSifraProizvodaCena.setText(cena.getSifraProizvoda() + "");
        txtSifraCene.setText(cena.getSifraCene() + "");
        txtVrednost.setText(cena.getVrednost() + "");
        txtSifraPDV.setText(cena.getSifraPDV() + "");

    }

    private void popuniProizvod() {
        int p = tblProizvod.getSelectedRow();
        TableProizvod tp = (TableProizvod) tblProizvod.getModel();
        Proizvod proizvod = tp.getProizvod(p);

        txtSifraProizvoda.setText(proizvod.getSifraProizvoda() + "");
        txtNazivProizvoda.setText(proizvod.getNazivProizvoda());
        txtZapremina.setText(proizvod.getZapremina() + "");
        txtOpis.setText(proizvod.getOpis());
        txtTipProizvoda.setText(proizvod.getTipProizvoda() + "");
        txtJedinicaMereStr.setText(proizvod.getJedMereStr());
    }
