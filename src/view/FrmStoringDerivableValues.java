    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        try {
            StavkaRacuna sr = new StavkaRacuna();
            sr.setRedniBrojStavkeRacuna(Long.parseLong(txtRedniBroj.getText().trim()));
            sr.setSifraRacuna(Long.parseLong(txtSifraRacuna.getText().trim()));
            sr.setKolicina(Integer.parseInt(txtKolicina.getText().trim()));
            sr.setBarkod(txtBarkod.getText().trim());
            sr.setUkupnaNaknada(Double.parseDouble(txtUkupnaNaknada.getText().trim()));
            sr.setRabat(Double.parseDouble(txtRabat.getText().trim()));
            sr.setProdajnaCenaSaRabatom(Double.parseDouble(txtProdajnaCenaSaRabatom.getText().trim()));
            sr.setPoreskaOsnovica(Double.parseDouble(txtPoreskaOsnovica.getText().trim()));
            sr.setSifraProizvoda(Long.parseLong(txtSifraProizvoda.getText().trim()));
            sr.setRedniBrojStavkePredracuna(Long.parseLong(txtRedniBrojStavkePredracuna.getText().trim()));
            sr.setBrojPredracuna(Long.parseLong(txtBrojPredracuna.getText().trim()));

            Controller.getInstance().saveStavkaRacuna(sr);
            JOptionPane.showMessageDialog(this, "Stavka racuna successfully updated!", "update stavkaRacuna", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            ViewController.getInstance().openFrmStoringDerivableValues();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Stavka racuna not updated!", "Update nalog", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

