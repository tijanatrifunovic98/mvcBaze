    private void fillTableRACUNI() throws Exception {
        List<Racun> racuni = Controller.getInstance().getAllRacuni();
        TableRacun model = new TableRacun(racuni);
        tblRacun.setModel(model);
    }

    private void fillTableSTAVKERACUNA() throws Exception {
        List<StavkaRacuna> stavkeRacuna = Controller.getInstance().getAllStavkeRacuna();
        TableStavkaRacuna model = new TableStavkaRacuna(stavkeRacuna);
        tblStavkeRacuna.setModel(model);
    }

